# Aliases related to the launching and usage of the vim editor

alias v='sudo vim'
alias vbashrc='v /etc/bash/bashrc.d/bashrc.sh'
alias vxinitrc='v /etc/X11/xinit/xinitrc'
alias vi3config='v /etc/i3/config'
alias vXresources='v /etc/X11/xinit/Xresources'
alias vvimrc='v /etc/vim/vimrc.local'
alias vblocks='v /etc/i3blocks.conf'
alias vmain='sudo vim ./main.cpp'
